package com.company;

public class Q2OneDimensionalArray {
    public static void main(String[] args) {
        int a[] = new int[5];
        a[0] = 1;
        a[1] = 0;
        a[2] = -5;
        a[3] = 10;

        System.out.println("Elements of array are: ");
        for (int i : a) {
            System.out.println(i);
        }
    }
}


